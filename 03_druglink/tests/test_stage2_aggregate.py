def test_stage3s_own_artifact_class_is_what_the_firewall_reads(admitted, honest):
    """Stage 2 ships NO artifact_class; the class is Stage-3's declaration about its run.

    The retired fixture firewall keyed on ``manifest["artifact_class"]`` — a field that has
    never existed in a Stage-2 manifest, so it could only ever have read a fixture's own
    invention.
    """
    import json
    manifest = json.load(open(honest["manifest_path"]))
    assert "artifact_class" not in manifest        # the field the old loader keyed on
    assert admitted.artifact_class == "fixture"    # Stage-3's own --artifact-class


def test_a_fixture_run_is_refused_by_the_analysis_path(admitted):
    assert admitted.artifact_class == "fixture"    # non-vacuous
    with pytest.raises(sa.AggregateAdmissionRefused) as exc:
        sa.require_analysis(admitted)
    assert sa.GATE_FIXTURE_FIREWALL in str(exc.value)


def test_an_analysis_run_passes_the_firewall(honest):
    """The same REAL release, declared by Stage 3 as an analysis, walks the analysis path."""
    agg = sa.admit_aggregate(**honest, artifact_class="analysis")
    assert sa.require_analysis(agg) is agg
    assert len(agg.arms) == 300                   # non-vacuous


def test_an_unknown_artifact_class_is_refused(honest):
    from druglink import artifact_class as ac
    with pytest.raises(ac.ArtifactClassError):
        sa.admit_aggregate(**honest, artifact_class="production")


def test_the_stage1_release_is_bound_by_its_canonical_hash(admitted):
    assert len(admitted.stage1_release_sha256) == 64
    # exactly what the real manifest pins
    assert admitted.stage1_release_sha256.startswith("1624471068d74c99")


def test_a_second_build_of_the_same_release_is_byte_identical(tmp_path):
    """Determinism: the release is content-addressed, so two materialisations agree."""
    a = sa.admit_aggregate(**build_release(tmp_path / "a"))
    b = sa.admit_aggregate(**build_release(tmp_path / "b"))
    assert a.manifest_self_hash == b.manifest_self_hash
    assert len(a.arms) == len(b.arms) == 300
    assert {x.bundle_id for x in a.bundles} == {x.bundle_id for x in b.bundles}
