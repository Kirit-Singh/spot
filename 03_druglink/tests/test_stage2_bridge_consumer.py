class TestTheDuplicateNativeIdentity:
    def test_two_native_records_under_one_identity_are_REFUSED(self, forged):
        from dataclasses import replace
        aggregate = _aggregate(forged)
        assert sb.native_index(aggregate), "non-vacuous: the native bytes carry rows"

        arm = next(a for a in aggregate.arms if a.lane in sa.MEASURED_LANES)
        doubled = replace(arm, records=arm.records + (dict(arm.records[0], arm_value=-1.0),))
        broken = replace(aggregate, arms=tuple(
            doubled if a.arm_key == arm.arm_key and a.lane == arm.lane else a
            for a in aggregate.arms))
        with pytest.raises(sa.Stage2BridgeError, match=sb.GATE_DUPLICATE_NATIVE_ROW):
            sb.native_index(broken)


