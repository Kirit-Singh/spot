# NON-VACUITY. The untouched release admits, and there is real evidence on BOTH sides.
# --------------------------------------------------------------------------- #
class TestTheUntouchedReleaseAdmits:
    def test_the_bridge_admits_and_carries_real_rows_over_real_native_records(self, forged):
        aggregate = _aggregate(forged)
        native = sb.native_index(aggregate)
        bridge = _admit(forged, aggregate)

        assert len(native) > 0, "the native bytes carry no rows: every gate below would be vacuous"
        assert bridge.n_rows == len(native), (
            "the bridge must carry EXACTLY the rows the admitted native bytes produce")
        assert bridge.n_rows >= 1000 and bridge.n_pathway_contexts > 0
        assert bridge.verdict == "admit"

    def test_every_typed_row_ADDS_a_namespace_and_a_modality_the_native_rows_lack(self, forged):
        aggregate = _aggregate(forged)
        native = sb.native_index(aggregate)
        # the native record states the measurement, and NOTHING about identity or assay
        one = next(iter(native.values()))
        assert set(one) == {"target_id", "arm_value", "evaluable", "rank"}

        typed = sa.typed_aggregate(aggregate, _admit(forged, aggregate))
        rows = [r for a in typed.arms if a.lane in sa.MEASURED_LANES for r in a.records]
        assert rows
        assert {r["target_id_namespace"] for r in rows} <= {"ensembl_gene_id", "gene_symbol"}
        assert {r["observed_perturbation_modality"] for r in rows} == {"CRISPRi_knockdown"}
        # ...and it carries the identity of the bridge that typed it
        assert typed.bridge_binding["bridge_raw_sha256"]

    def test_the_universe_is_HETEROGENEOUS_so_a_shape_guess_would_mistype_it(self, forged):
        typed = sa.typed_aggregate(_aggregate(forged), _admit(forged))
        rows = [r for a in typed.arms if a.lane in sa.MEASURED_LANES for r in a.records]
        namespaces = {r["target_id_namespace"] for r in rows}
        assert namespaces == {"ensembl_gene_id", "gene_symbol"}, (
            "the fixture must carry BOTH namespaces: a namespace guessed from an id's shape "
            "mistypes exactly the bare symbols, and three of the four carry an ENSG-looking key "
            "belonging to a DIFFERENT gene")


