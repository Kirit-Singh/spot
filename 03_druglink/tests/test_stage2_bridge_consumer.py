class TestTheBridgeBytes:
    def test_bridge_bytes_edited_after_sealing(self, forged):
        doc = json.load(open(forged["stage2_bridge"]))
        doc["n_target_rows"] = 1                      # edited, and NOT re-sealed
        with open(forged["stage2_bridge"], "w") as fh:
            json.dump(doc, fh)
        _refuses(forged, sb.GATE_BRIDGE_SELF_HASH)

    def test_a_bridge_that_binds_nothing(self, forged):
        W3F.reseal_bridge(forged["stage2_bridge"],
                          lambda d: d["bindings"].pop("aggregate"))
        _refuses(forged, sb.GATE_BRIDGE_BINDS_NOTHING)

    def test_a_CONSISTENT_reseal_still_fails_the_report_that_judged_the_old_bytes(self, forged):
        """The forger re-seals perfectly. The REPORT still names the bytes it actually judged."""
        W3F.reseal_bridge(forged["stage2_bridge"],
                          lambda d: d["target_rows"][0].update({"arm_value": -99.0}))
        _refuses(forged, sb.GATE_REPORT_JUDGED_OTHER_BYTES)


