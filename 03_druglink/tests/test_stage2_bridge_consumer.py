@pytest.fixture(scope="module")
def release(tmp_path_factory):
    """ONE admitted release + bridge, built once by W3's real producers. Attacks work on copies."""
    return W3F.build_release(str(tmp_path_factory.mktemp("w3_release")))


@pytest.fixture()
def forged(release, tmp_path):
    """A private copy of the whole release, so an attack cannot leak into another test."""
    root = str(tmp_path / "release")
    shutil.copytree(os.path.dirname(release["bundles_root"]), root)
    bundles = os.path.join(root, os.path.basename(release["bundles_root"]))
    return {"bundles_root": bundles,
            "stage2_manifest": os.path.join(bundles, W3F.MANIFEST_FILE),
            "stage2_report": os.path.join(bundles, W3F.AGGREGATE_REPORT_FILE),
            "stage1_release": release["stage1_release"].replace(
                os.path.dirname(release["bundles_root"]), root),
            "stage2_bridge": os.path.join(bundles, W3F.BRIDGE_FILE),
            "stage2_bridge_report": os.path.join(bundles, W3F.BRIDGE_REPORT_FILE),
            "stage2_bridge_receipt": os.path.join(bundles, W3F.RECEIPT_FILE)}


def _aggregate(paths, artifact_class="analysis"):
    return sa.admit_aggregate(
        manifest_path=paths["stage2_manifest"], report_path=paths["stage2_report"],
        bundles_root=paths["bundles_root"], stage1_release_path=paths["stage1_release"],
        artifact_class=artifact_class)


def _admit(paths, aggregate=None):
    aggregate = aggregate or _aggregate(paths)
    return sa.admit_bridge(
        bridge_path=paths["stage2_bridge"], report_path=paths["stage2_bridge_report"],
        receipt_path=paths["stage2_bridge_receipt"], aggregate=aggregate,
        aggregate_report_path=paths["stage2_report"])


def _refuses(paths, gate, aggregate=None):
    with pytest.raises(sa.Stage2BridgeError, match=gate) as exc:
        _admit(paths, aggregate)
    return exc.value


