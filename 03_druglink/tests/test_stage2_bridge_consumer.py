class TestTheAggregateCrossBind:
    def test_a_bridge_built_over_a_DIFFERENT_aggregate(self, forged):
        """Every self-hash inside the chain agrees. It is simply not THIS release's bridge."""
        other = "a" * 64
        W3F.reseal_bridge(
            forged["stage2_bridge"],
            lambda d: d["bindings"]["aggregate"]["manifest"].update({"raw_sha256": other}))
        # ...and the forger re-points the report and the receipt at the resealed bridge, so the
        # ONLY thing left standing between them and an admit is the cross-bind.
        raw = sb.canonical_sha256  # noqa: F841  (documents the rule the fixture re-seals under)
        import hashlib
        with open(forged["stage2_bridge"], "rb") as fh:
            bridge_raw = hashlib.sha256(fh.read()).hexdigest()
        W3F.rewrite(forged["stage2_bridge_report"],
                    lambda d: d["judged_bridge"].update({"raw_sha256": bridge_raw}))
        W3F.reseal_receipt(forged["stage2_bridge_receipt"],
                           lambda d: d["bridge"].update({"raw_sha256": bridge_raw}))
        exc = _refuses(forged, sb.GATE_RECEIPT_BINDS_OTHER_BYTES)
        assert "canonical" in str(exc) or "bridge" in str(exc)

    def test_the_bridge_names_the_aggregate_stage3_admitted(self, forged):
        aggregate = _aggregate(forged)
        bound = json.load(open(forged["stage2_bridge"]))["bindings"]["aggregate"]["manifest"]
        assert bound["raw_sha256"] == aggregate.manifest_raw_sha256


