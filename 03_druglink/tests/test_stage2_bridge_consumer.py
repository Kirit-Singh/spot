"""THE BRIDGE CONSUMER, under attack. Every gate must FIRE on a CONSISTENT forgery.

The bridge, its report and its receipt here are generated by driving **W3's REAL producer and
their REAL independent verifiers** (``tests/w3_bridge_fixture``). Nothing is hand-authored: a
hand-written fixture is how Stage 3 once invented an entire Stage-2 manifest schema and tested
itself against its own fiction.

NON-VACUITY. Every attack first asserts that the UNTOUCHED release ADMITS, with rows on both
sides — a gate that fires on an empty bridge has proved nothing.

Each forgery is RE-SEALED (``reseal_bridge`` / ``reseal_receipt``), because a forger with repo
access can recompute a self-hash. The gates must stop a CONSISTENT forgery, not a careless one.
"""
