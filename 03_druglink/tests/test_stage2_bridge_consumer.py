def _repoint(paths) -> None:
    """Re-point the report and the receipt at the RESEALED bridge — the strongest attack.

    The forger owns the whole chain: the bridge re-seals, the report is made to name the new
    bytes, and the receipt binds them. Every hash agrees with every other hash. The ONLY thing
    left is whether the rows still agree with the bytes Stage 2 actually admitted.
    """
    import hashlib
    with open(paths["stage2_bridge"], "rb") as fh:
        raw = hashlib.sha256(fh.read()).hexdigest()
    doc = json.load(open(paths["stage2_bridge"]))
    canonical = sb.canonical_sha256(doc)
    W3F.rewrite(paths["stage2_bridge_report"],
                lambda d: d["judged_bridge"].update({"raw_sha256": raw,
                                                     "canonical_sha256": canonical}))
    with open(paths["stage2_bridge_report"], "rb") as fh:
        report_raw = hashlib.sha256(fh.read()).hexdigest()

    def fix(d):
        d["bridge"].update({"raw_sha256": raw, "canonical_sha256": canonical})
        d["bridge_report"].update({"raw_sha256": report_raw})
    W3F.reseal_receipt(paths["stage2_bridge_receipt"], fix)
