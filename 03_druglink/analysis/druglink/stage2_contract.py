"""The NATIVE Stage-2 contract, and the BYTES that prove it.

The vocabulary of Stage-2's real aggregate release — its schemas, its named refusal gates,
its content-hash rule, its slot algebra — plus the code that re-reads the 15 all-arm bundle
DIRECTORIES from disk and proves every byte the manifest bound.

This is the lower layer. It answers "what IS this artifact, and are these its bytes?".
Whether Stage 2 ADMITTED it is a separate question, asked by :mod:`druglink.stage2_aggregate`,
which imports from here. Splitting them keeps each module to one purpose (and under the
500-line rule); the public API stays on ``stage2_aggregate``, which re-exports these names.

The contract was replayed from Stage-2's ACTUAL generated bytes — not from its source, and
certainly not from a shape Stage 3 invented for itself. See ``stage2_aggregate`` for the full
account of what that invented shape was and why it could never ingest a real release.
"""
