"""Disk-backed admission of the REAL Stage-2 aggregate release. THE production gate.

This module previously parsed a Stage-2 schema that **does not exist**. It read
``inventory[]``, ``stage1_release.raw_sha256``, ``artifact_class`` and an ``admits{}``
block, and it required the substring ``"independent"`` in the verifier id. Stage 2 emits
none of those. Its tests passed only because one author wrote both the loader and the
fixture it parsed — a self-consistent fiction that could not ingest one byte of the real
thing.

The contract below is TRANSCRIBED FROM STAGE-2's ACTUAL GENERATED BYTES — the manifest and
the admission report its real CLIs emitted and its own separate verifier admitted — not from
a shape Stage 3 invented for itself. The test fixture parses those same bytes verbatim
(``tests/stage2_release_fixture.py``), so the loader cannot drift from the producer without a
test failing. That is the whole repair: the old fixture COULD drift, and did, silently.

THE NATIVE CONTRACT
-------------------
manifest  ``spot.stage02_run_manifest.v3_topology_only`` — top-level ``bundles[]`` (each an
          all-arm bundle DIRECTORY: an ``out_dir`` NAME + a ``files{}`` map + ``arm_keys``),
          the bound ``stage1_v3_release``, and a ``manifest_sha256`` that is the producer's
          SEMANTIC self-hash: the canonical content hash of the document EXCLUDING
          ``created_at``, ``manifest_sha256`` and ``path``. We RE-DERIVE it, never read it.
report    ``spot.stage02_run_manifest_verification.v1`` — a SEPARATE artifact from
          ``spot.stage02.run_manifest.verifier.v1``. Its id does not contain the word
          "independent", and never did: independence is asserted by the STRUCTURED FIELD
          ``generator_is_not_verifier``. A name is not a binding, so THAT is what we bind.

WHAT WE DELIBERATELY DO **NOT** BIND. Stage-2's *source* defines a ``stage3_inputs`` handoff
receipt (``spot.stage02.stage3_handoff.v1``), and it would be the natural thing to consume —
but it is **ABSENT from the actually-generated report**. Binding a field the real producer
does not emit is the very error this module exists to fix, so Stage 3 binds the FOUR PATHS
EXPLICITLY (manifest, report, bundles root, Stage-1 release) and reads nothing that the bytes
on disk do not contain. If Stage 2 ever ships the receipt, that is the day to bind it.

WHAT ADMITS A RELEASE. Stage 2 declares no ``artifact_class``, so the old fixture-firewall
keyed on a field that does not exist. The real gate is Stage-2's own admission, which a
fixture cannot forge out of Stage-2's real verifier:

    verdict == "admit"  AND  admission.status == "admitted"
    AND generator_is_not_verifier is True   AND  n_failed == 0
    AND topology_complete is True           AND  release_admissible is True
    AND the report's manifest_sha256 == the semantic self-hash WE recompute from the
        manifest bytes on disk

Stage-3's OWN ``--artifact-class`` stays (Stage-3's declaration about Stage-3's output,
never Stage-2's) and :func:`require_analysis` still enforces it.

Every refusal is a NAMED gate. Fail closed. A missing field is a refusal, never a default.
"""
