# --- 1. Identity, and STAGE-2's OWN ADMISSION. ------------------------------- #
def _check_manifest_identity(manifest: dict[str, Any]) -> str:
    """It recomputes its own semantic identity, or nothing it says about anything stands."""
    declared = manifest.get(SELF_HASH_FIELD)
    recomputed = manifest_self_hash(manifest)
    if declared != recomputed:
        _refuse(GATE_MANIFEST_SELF_HASH,
                f"the manifest declares {SELF_HASH_FIELD}={str(declared)[:16]}… but its own "
                f"semantic content hashes to {recomputed[:16]}…. A manifest that cannot prove "
                "its own identity is not a root of trust; it is a document asserting a "
                "number.")
    return recomputed


def _check_report(report: dict[str, Any], *, self_hash: str) -> tuple[str, str]:
    """Stage-2's OWN admission. Every clause is a separate, named gate."""
    verifier_id = str(report.get("verifier_id") or "")
    if verifier_id != AGGREGATE_VERIFIER_ID:
        _refuse(GATE_VERIFIER_NOT_PINNED,
                f"the report is signed {verifier_id!r}; the pinned aggregate verifier is "
                f"{AGGREGATE_VERIFIER_ID!r}.")
    # INDEPENDENCE IS A STRUCTURED FIELD, NOT A NAME. The retired gate required the substring
    # "independent" in the verifier id — which the real verifier's id does not contain, so it
    # would have REFUSED the genuine report while ADMITTING any forgery that merely named
    # itself "…independent…". A substring proves nothing; this field is the assertion.
    if report.get("generator_is_not_verifier") is not True:
        _refuse(GATE_GENERATOR_IS_VERIFIER,
                "the report does not assert generator_is_not_verifier=true. A producer "
                "agreeing with itself is the one thing an independent verifier exists to rule "
                "out, and a missing assertion is a refusal, never a default.")
    verdict = report.get("verdict")
    if verdict != ADMIT:
        _refuse(GATE_VERDICT_NOT_ADMIT,
                f"the aggregate verifier's verdict is {verdict!r}, not {ADMIT!r}")
    if report.get("n_failed") != 0:
        _refuse(GATE_GATES_FAILED,
                f"the aggregate verifier recorded n_failed={report.get('n_failed')!r} "
                f"({report.get('failed_gates')}); a release with a failed gate is not "
                "admitted, whatever its verdict string says.")
    if report.get("topology_complete") is not True:
        _refuse(GATE_TOPOLOGY_NOT_COMPLETE,
                f"the verifier found topology_complete={report.get('topology_complete')!r}: "
                "a partial run is never release-admissible.")
    if report.get("release_admissible") is not True:
        _refuse(GATE_NOT_RELEASE_ADMISSIBLE,
                f"the verifier found release_admissible="
                f"{report.get('release_admissible')!r}.")
    status = (report.get("admission") or {}).get("status")
    if status != ADMITTED:
        _refuse(GATE_ADMISSION_NOT_GRANTED,
                f"admission.status is {status!r}, not {ADMITTED!r}. Admission is granted by "
                "the independent aggregate report, or not at all.")
    # THE REPORT MUST ADMIT *THESE* BYTES. Both its claim and its own recomputation must equal
    # the self-hash WE derived from the manifest on disk — so a report about some other
    # manifest, and a manifest edited after it was judged, are the same refusal.
    claimed = report.get(SELF_HASH_FIELD)
    recomputed = report.get("manifest_sha256_recomputed")
    if claimed != self_hash or recomputed != self_hash:
        _refuse(GATE_REPORT_BINDS_ANOTHER_MANIFEST,
                f"the report admits manifest {str(claimed)[:16]}… (it recomputed "
                f"{str(recomputed)[:16]}…), but the manifest on disk semantically hashes to "
                f"{self_hash[:16]}…. An ADMIT that names other bytes is an opinion about some "
                "other artifact.")
    return verifier_id, str(verdict)


def _check_stage1_release(manifest: dict[str, Any], stage1_release_path: str) -> str:
    """The release the aggregate was computed against, bound to the bytes it names."""
    bound = manifest.get("stage1_v3_release") or {}
    pinned = bound.get("release_canonical_sha256")
    if not pinned:
        _refuse(GATE_STAGE1_RELEASE_UNBOUND,
                "the manifest pins no stage1_v3_release.release_canonical_sha256: an "
                "aggregate that cannot name the release it stands on cannot be replayed "
                "against it.")
    release, raw = C.load_json(stage1_release_path, "pinned Stage-1 v3 release")
    on_disk = stage2_content_sha256(release)
    if on_disk != pinned:
        _refuse(GATE_STAGE1_RELEASE_UNBOUND,
                f"the Stage-1 release on disk canonically hashes to {on_disk[:16]}… but the "
                f"manifest pins {str(pinned)[:16]}…: a DIFFERENT release than the aggregate "
                "was computed against.")
    declared_raw = bound.get("release_raw_sha256")
    if declared_raw and declared_raw != raw:
        _refuse(GATE_STAGE1_RELEASE_UNBOUND,
                f"the Stage-1 release BYTES on disk hash to {raw[:16]}… but the manifest bound "
                f"{str(declared_raw)[:16]}…: the file was re-serialised after the run.")
    return on_disk



# --- The gate. -------------------------------------------------------------- #
def admit_aggregate(*, manifest_path: str, report_path: str, bundles_root: str,
                    stage1_release_path: str,
                    artifact_class: str = ac.FIXTURE) -> AdmittedAggregate:
    """Admit the REAL Stage-2 aggregate FROM DISK, or refuse by name.

    ``artifact_class`` is STAGE-3's own declaration about what Stage 3 is doing with this
    release (its ``--artifact-class`` flag). Stage 2 declares none, and never did: the gate on
    the RELEASE is Stage-2's own admission, checked in :func:`_check_report`.
    """
    klass = ac.require(str(artifact_class or ""))
    if os.path.realpath(manifest_path) == os.path.realpath(report_path):
        _refuse(GATE_SELF_ADMISSION,
                "the report and the manifest are the SAME file. A producer does not admit "
                "itself: the report is a separate artifact from a separate verifier.")

    manifest, manifest_raw = C.load_json(manifest_path, "Stage-2 aggregate run manifest")
    C.require_schema(manifest, MANIFEST_SCHEMA, GATE_MANIFEST_NOT_NATIVE,
                    "Stage-2 aggregate run manifest")
    report, _ = C.load_json(report_path, "Stage-2 aggregate verification report")
    C.require_schema(report, REPORT_SCHEMA, GATE_REPORT_NOT_NATIVE,
                    "Stage-2 aggregate verification report")

    self_hash = _check_manifest_identity(manifest)
    verifier_id, verdict = _check_report(report, self_hash=self_hash)
    stage1_sha = _check_stage1_release(manifest, stage1_release_path)
    programs, conditions, sources = C.release_topology(manifest)

    manifest_canonical = stage2_content_sha256(manifest)
    provenance = {"manifest_raw_sha256": manifest_raw,
                  "manifest_canonical_sha256": manifest_canonical,
                  "manifest_self_hash": self_hash, "aggregate_verifier_id": verifier_id,
                  "aggregate_verdict": verdict, "stage1_release_sha256": stage1_sha}

    bound = C.check_bundles(manifest, bundles_root, conditions=conditions, sources=sources)
    arms: list[LoadedArm] = []
    for bundle, full, entry in bound:
        arms.extend(C.load_bundle_arms(bundle, full, entry, provenance=provenance))
    bundles = [b for b, _f, _e in bound]
    program_ids = C.check_arm_topology(arms, bundles, programs)

    return AdmittedAggregate(
        artifact_class=klass, manifest_raw_sha256=manifest_raw,
        manifest_canonical_sha256=manifest_canonical, manifest_self_hash=self_hash,
        verifier_id=verifier_id, verdict=verdict, stage1_release_sha256=stage1_sha,
        bundles=tuple(bundles), arms=tuple(arms), program_ids=program_ids,
        counts={"n_bundles": len(bundles), "n_arm_slots": len(arms),
                "n_programs": len(program_ids),
                "bundles_per_lane": {ln: sum(b.lane == ln for b in bundles) for ln in LANES},
                "arms_per_lane": {ln: sum(a.lane == ln for a in arms) for ln in LANES},
                "topology_is_derived_not_declared": True,
                "combined_objective_permitted": False, "pair_roles_assigned": False})


def require_analysis(admitted: AdmittedAggregate) -> AdmittedAggregate:
    """Stage-3's fixture firewall, on STAGE-3's own class: a sealed test run never becomes a
    real analysis. (This is not the Stage-2 gate — that is the admission above.)"""
    if admitted.artifact_class != ac.ANALYSIS:
        _refuse(GATE_FIXTURE_FIREWALL,
                f"the run declares artifact_class={admitted.artifact_class!r}; only an "
                "'analysis' run may enter the analysis path — a synthetic number on its way to "
                "Stage 4 is a fabricated candidate.")
    return admitted
